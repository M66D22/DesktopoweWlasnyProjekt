    private void md_jMenuItemZapiszActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_md_jMenuItemZapiszActionPerformed
        try {
            FileWriter fw = new FileWriter(fileSettings);
            
            String login = md_jTextFieldLogin.getText();
            String oUzytkowniku = md_jTextAreaInformacjeDoZapisu.getText();
            int jakiObraz = nrObrazka;
            
            fw.write(""+login+";"+oUzytkowniku+";"+";"+jakiObraz);
            fw.close();
            
        } catch (IOException ex) {
            Logger.getLogger(WlasnaAplikacja.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_md_jMenuItemZapiszActionPerformed

