    private void md_jComboBoxAwatarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_md_jComboBoxAwatarActionPerformed
        javax.swing.JComboBox cb = (javax.swing.JComboBox)evt.getSource();
        int nr = cb.getSelectedIndex();
        String name = (String) cb.getSelectedItem();
        
        ImageIcon icon = new ImageIcon (AbsolutePathResources+name+".png");
        switch (nr){
            case 0:
                md_jLabelAwatar.setIcon(new javax.swing.ImageIcon(AbsolutePathResources+"1Awatar.png"));
                nrObrazka = 1;
                break;
            case 1:
                md_jLabelAwatar.setIcon(new javax.swing.ImageIcon(AbsolutePathResources+"2Awatar.png"));
                nrObrazka = 2;
                break;
            case 2:
                md_jLabelAwatar.setIcon(new javax.swing.ImageIcon(AbsolutePathResources+"3Awatar.png"));
                nrObrazka = 3;
                break;
            case 3:
                md_jLabelAwatar.setIcon(new javax.swing.ImageIcon(AbsolutePathResources+"4Awatar.png"));
                nrObrazka = 4;
                break;
        }
        
        
    }//GEN-LAST:event_md_jComboBoxAwatarActionPerformed

